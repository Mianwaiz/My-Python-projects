#class
class Calculator:
	def add(self,num1,num2):
		return num1+num2
	def sub(self,num1,num2):
		return num1-num2
	def multi(self,num1,num2):
		return num1*num2
	def divide(self,num1,num2):
		return num1/num2
#objects
